def get_dimension_prompt(dimension_key: str, raw_data: str = "", professional_data: dict = None) -> str:
    """
    获取指定维度的 Prompt 模板

    Args:
        dimension_key: 维度键名
        raw_data: 原始村庄数据
        professional_data: 来自黑板的专业数据（可选）

    Returns:
        格式化后的 Prompt 字符串
    """
    if dimension_key not in ANALYSIS_DIMENSIONS:
        raise ValueError(f"未知的分析维度: {dimension_key}")

    prompt = ANALYSIS_DIMENSIONS[dimension_key]["prompt"]

    # 生成专业数据部分
    professional_data_section = _generate_professional_data_section(dimension_key, professional_data)

    return prompt.format(
        raw_data=raw_data,
        professional_data_section=professional_data_section
    )


def _generate_professional_data_section(dimension_key: str, professional_data: dict = None) -> str:
    """
    生成专业数据部分的 Prompt 文本

    Args:
        dimension_key: 维度键名
        professional_data: 来自黑板的专业数据

    Returns:
        专业数据部分的 Prompt 文本
    """
    if not professional_data:
        return ""

    sections = []

    # 自然环境维度的专业数据
    if dimension_key == "natural_environment":
        # 土壤分析数据
        if professional_data.get("soil_analysis"):
            soil = professional_data["soil_analysis"]
            sections.append(f"""
**【专业数据 - 土壤分析】**：
以下信息来自GIS空间分析，请在分析中参考：
- 土壤类型分布：{', '.join([s['type'] for s in soil.get('soil_types', [])])}
- 土壤质量指数：{soil.get('soil_quality_index', 'N/A')}
- 水土流失风险：{soil.get('erosion_risk', 'N/A')}
""")

        # 水文分析数据
        if professional_data.get("hydrology_analysis"):
            hydro = professional_data["hydrology_analysis"]
            water_systems = hydro.get("water_systems", [])
            sections.append(f"""
**【专业数据 - 水文分析】**：
以下信息来自GIS空间分析，请在分析中参考：
- 主要水系：{', '.join([w['name'] for w in water_systems])}
- 水体类型：{', '.join([w['type'] for w in water_systems])}
- 洪水风险区域：{len(hydro.get('flood_risk_areas', []))} 个
""")

    # 社会经济维度的专业数据
    elif dimension_key == "socio_economic":
        # 人口结构数据
        if professional_data.get("population_structure"):
            pop = professional_data["population_structure"]
            age_dist = pop.get("age_distribution", [])
            age_str = ', '.join([f"{a['age_group']}占{a['percentage']}%" for a in age_dist])
            sections.append(f"""
**【专业数据 - 人口结构模型】**：
以下信息来自人口预测分析，请在分析中参考：
- 年龄结构：{age_str}
- 劳动参与率：{pop.get('labor_force_participation_rate', 'N/A')}%
- 抚养比：{pop.get('dependency_ratio', 'N/A')}%
""")

        # 劳动力供给数据
        if professional_data.get("labor_force_analysis"):
            labor = professional_data["labor_force_analysis"]
            sections.append(f"""
**【专业数据 - 劳动力供给分析】**：
以下信息来自劳动力分析，请在分析中参考：
- 劳动年龄人口：{labor.get('working_age_population', 'N/A')} 人
- 劳动力规模：{labor.get('labor_force_size', 'N/A')} 人
- 劳动力剩余：{labor.get('labor_surplus', 'N/A')} 人
- 老龄化趋势：{labor.get('aging_trend', 'N/A')}
""")

    # 土地利用维度的专业数据
    elif dimension_key == "land_use":
        if professional_data.get("land_use_analysis"):
            land_use = professional_data["land_use_analysis"]
            land_types = land_use.get("land_use_types", [])
            land_type_str = ', '.join([f"{lt['type']}占{lt['percentage']}%" for lt in land_types])
            sections.append(f"""
**【专业数据 - 土地利用分析】**：
以下信息来自GIS空间分析，请在分析中参考：
- 总面积：{land_use.get('total_area', 'N/A')} 平方公里
- 主要用地类型：{land_type_str}
- 土地利用效率：{land_use.get('land_use_efficiency', 'N/A')}
- 开发强度：{land_use.get('development_intensity', 'N/A')}
""")

    # 道路交通维度的专业数据
    elif dimension_key == "traffic":
        if professional_data.get("connectivity_metrics"):
            conn = professional_data["connectivity_metrics"]
            sections.append(f"""
**【专业数据 - 路网连通度分析】**：
以下信息来自网络分析，请在分析中参考：
- 路网密度：{conn.get('network_density', 'N/A')} 公里/平方公里
- 连通性指数：{conn.get('connectivity_index', 'N/A')}
- 可达性评分：{conn.get('accessibility_score', 'N/A')}
""")

        if professional_data.get("accessibility_analysis"):
            acc = professional_data["accessibility_analysis"]
            service_areas = acc.get("service_areas", [])
            service_area_str = ', '.join([f"{sa['center']}覆盖{sa['coverage_ratio']}%" for sa in service_areas])
            sections.append(f"""
**【专业数据 - 可达性分析】**：
以下信息来自网络分析，请在分析中参考：
- 服务区域覆盖：{service_area_str}
""")

    if sections:
        return "\n".join(sections)

    return ""


