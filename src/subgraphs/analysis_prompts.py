# ==========================================
# 辅助函数
# ==========================================

def get_dimension_prompt(dimension_key: str, raw_data: str = "") -> str:
    """获取指定维度的 Prompt 模板"""
    if dimension_key not in ANALYSIS_DIMENSIONS:
        raise ValueError(f"未知的分析维度: {dimension_key}")

    prompt = ANALYSIS_DIMENSIONS[dimension_key]["prompt"]
    return prompt.format(raw_data=raw_data)


def list_dimensions():
    """列出所有分析维度"""
    return [
        {
            "key": key,
            "name": value["name"],
            "description": value["description"]
        }
        for key, value in ANALYSIS_DIMENSIONS.items()
    ]


